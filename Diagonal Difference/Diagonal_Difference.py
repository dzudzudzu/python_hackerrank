def diagonalDifference(arr):
    # Write your code here
    suma1=0
    suma2=0
    x = len(arr) -1 
    for i in range(0, len(arr)):
        suma1 = suma1 + arr[i][i] 
        suma2 = suma2 + arr[i][x-i]
        
    return abs(suma2-suma1)

